package TSwitchCase;

public class MySwitchCase {
	/* Serve para Operações exatas */
	public static void main(String[] args) {
		int dia = 2;
		switch (dia) {
		case 1:
			System.out.println("Domingo");
			break;
		case 2:
			System.out.println("Segunda-feira");
			break;
		default:
			System.out.println("Ops");
			break;
		}
	}
}
